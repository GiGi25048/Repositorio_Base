# Utilize um loop while e um loop for para contar de 0 até o número que o usuário digitar:

# ------------------------------------------ ESCREVA SEU CÓDIGO ABAIXO -----------------------------------------------------------

# LOOP WHILE

fim= int(input('digite um numero:'))
n = 0
while n <= fim:
    print(n)
    n= n+1
    








# LOOP FOR

for n in range(fim+1):
    print(n)